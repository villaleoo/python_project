class User :
    
   
    def __init__(self,  name, email, password):
      self.__name= name;
      self.__email= email;
      self.__password= password;
      
     
    
    
    def get_name(self):
      return self.__name;
    
    def set_name(self, name):
      self.__name= name;
      
    def get_email(self):
      return self.__email;
    
    def set_email(self, email):
      self.__email=email;
      
    def get_password(self):
      return self.__password;
    
    def set_password(self, psw):
      self.__password=psw;
       
  
   
    
 



